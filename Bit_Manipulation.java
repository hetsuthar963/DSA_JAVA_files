import java.util.*;
public class Bit_Manipulation{
    public static void main(String args[]){
        System.out.println((5 & 6));//AND bit-wise operator
        System.out.println((5 | 6));//OR bit-wise operator
        System.out.println((5 ^ 6));//XOR bit-wise operator
        System.out.println((~0));//NOT bit-wise operator
        System.out.println((5<<2));//Left Shift <<
        System.out.println((6>>1));//Right Shift >>>

    }
}